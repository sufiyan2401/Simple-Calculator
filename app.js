
var x = prompt("Enter First Number")
var y = prompt("Enter Second Number")
let a = prompt("Enter an Operator Like -,+,*,/")
if(a == "+"){
    var z =Number(x)+Number(y);
    alert(z);
}
else if(a == "*"){
    var z =Number(x)*Number(y);
    alert(z);
}
else if(a == "-"){
    var z =Number(x)-Number(y);
    alert(z);
}
else if(a == "/"){
    var z =Number(x)/Number(y);
    alert(z);
}
